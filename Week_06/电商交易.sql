  `buy_cust_id` mediumint(9) NOT NULL COMMENT '买家会员标识',
  `sell_cust_id` mediumint(9) NOT NULL COMMENT '卖家会员标识',
  `consignee` varchar(20) NOT NULL COMMENT '收货人姓名',
