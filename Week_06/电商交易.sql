  `pay_id` int(11) NOT NULL COMMENT '付款方式标识',
  `send_mode` int(11) NOT NULL COMMENT '配送方式标识',
  `order_state` char(1) NOT NULL COMMENT '订单状态',
  `order_time` datetime NOT NULL COMMENT '下单时间',
  `pay_time` datetime DEFAULT NULL COMMENT '付款时间',
  `send_time` datetime DEFAULT NULL COMMENT '发货时间',
  `sure_time` datetime DEFAULT NULL COMMENT '确认时间',
  `mem_remark` varchar(300) DEFAULT NULL COMMENT '会员留言',
  `send_num` varchar(30) DEFAULT NULL COMMENT '物流编号',
  `is_virtual` char(1) DEFAULT NULL COMMENT '是否虚拟 0是1否',
  `order_type` char(1) DEFAULT NULL COMMENT '订单类型',
  `mark_id` varchar(12) DEFAULT NULL COMMENT '备注',
  PRIMARY KEY (`order_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='商品订单信息';
