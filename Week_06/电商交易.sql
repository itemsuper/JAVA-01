-- 用户表
CREATE TABLE `sysuser` (
  `user_id` mediumint(9) NOT NULL,
  `user_name` varchar(20) DEFAULT NULL COMMENT '用户名',
  `nike_name` varchar(10) DEFAULT NULL COMMENT '昵称',
  `passwd` char(32) DEFAULT NULL COMMENT '密码',
  `user_type` char(1) DEFAULT '4' COMMENT '3：管理员 4：子账号 ',
  `role_id` int(11) DEFAULT NULL COMMENT '角色id',
  `org_id` varchar(100) DEFAULT NULL COMMENT '机构id',
  `real_name` varchar(10) DEFAULT NULL COMMENT '真实姓名',
  `email` varchar(50) DEFAULT NULL COMMENT '邮箱',
  `loginnum` int(11) DEFAULT '0' COMMENT '登录次数',
  `logintime` datetime DEFAULT NULL COMMENT '最后一次登录时间',
  `loginip` varchar(20) DEFAULT NULL COMMENT '登录ip',
  `state` char(1) DEFAULT NULL COMMENT '0：启用 1：禁用',
  PRIMARY KEY (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='系统用户表';


-- 商品表
