  `zip_code` int(11) DEFAULT NULL COMMENT '邮编',
  `telephone` varchar(20) DEFAULT NULL COMMENT '电话',
  `mobile` int(11) DEFAULT NULL COMMENT '手机',
  `goods_amount` decimal(10,2) NOT NULL DEFAULT '0.00' COMMENT '商品总金额',
  `comm_free` decimal(10,2) DEFAULT NULL COMMENT '手续费费用',
  `ship_free` decimal(10,2) NOT NULL DEFAULT '0.00' COMMENT '配送费用',
